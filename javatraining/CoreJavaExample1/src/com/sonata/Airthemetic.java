package com.sonata;

public class Airthemetic {

	 public static void main(String[] args){
	
		  int i = 20;
		  int j = 10;
		  System.out.println("Add");
		  System.out.println("i + j= " + (i + j));
		 
		  System.out.println("Subtract");
		  System.out.println("i + j= " + (i - j));
		  
		  System.out.println("Divide");
		  System.out.println("i + j= " + (i / j));
		 
		  System.out.println("Multiply");
		  System.out.println("i + j= " + (i * j));  
		  
		  System.out.println("Arithemetic Operators");
		  System.out.println("i > j= "+(i>j)); // greater than
		  System.out.println("i < j= "+(i<j)); // lesser than
		  System.out.println("i >= j= "+(i>=j)); // greater than or equal to
		  System.out.println("i <= j= "+(i<=j)); // lesser than or equal to
		  System.out.println("i == j= " + (i==j)); //  equal to 
		  
		 
	 }
	
}
